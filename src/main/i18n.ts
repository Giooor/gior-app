function isAppLanguage(value: string | undefined): value is AppLanguage {
  return value === 'es' || value === 'en' || value === 'pt' || value === 'fr'
}

export function getMainLanguage(): AppLanguage {
  const value = getSetting('app_language')
  return isAppLanguage(value) ? value : 'es'
}

export function mt(key: string, vars?: Record<string, string | number>): string {
  const lang = getMainLanguage()
  let str = STRINGS[lang][key] ?? STRINGS.es[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{{${k}}}`, String(v))
    }
  }
  return str
}
